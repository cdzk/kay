// 自动加载各个模块的路由配置
foreach (scan_dir(APP_PATH) as $key=>$val)
{
    if (is_file(APP_PATH.$key.'/route.php')) require_once ($key.'/route.php');
}

return [
    '__pattern__' => [
        'name' => '\w+',
    ],
    '[hello]'     => [
        ':id'   => ['index/hello', ['method' => 'get'], ['id' => '\d+']],
        ':name' => ['index/hello', ['method' => 'post']],
    ],
];

